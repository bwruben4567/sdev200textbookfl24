package Chapter1.Exercise1;

public class MovieQuoteInfo {
    /* 
    This class demonstrates the use of the println() method 
    to print four messages containing my favorite movie quote, 
    the movie it comes from, the character who said it, and the year of the movie.
    */

    public static void main(String[] args)
    {
        System.out.println("QUOTE: 'In the beginning, the universe was created. This has made a lot of people very angry and been widely regarded as a bad move.'");
        System.out.println("MOVIE: The Hitchhiker's Guide to the Galaxy");
        System.out.println("CHARACTER: The Narrator");
        System.out.println("MOVIE YEAR: 2005");
    }
}